					<h2>Terminologie</h2>
					<p>
<ul>
<li>
Hyperviseur : Plate-forme de virtualisation qui permet à plusieurs systèmes d'exploitation de travailler sur une même machine physique en même temps, de manière isolée.
</li>
<li>
KVM (Kernel-based Virtual Machine) : Hyperviseur libre de type I
Utilise les instructions de virtualisation des processeurs x86 par Intel® et AMD®.</li>
</ul>

